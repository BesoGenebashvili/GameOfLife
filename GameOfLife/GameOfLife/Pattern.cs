namespace GameOfLife;

public static class Pattern
{
    private const int FixedWidth = 26;
    private const int FixedHeight = 26;

    public enum Name : byte
    {
        Random,
        Bunnies
    }

    public static int[][] Resolve(Name pattern) => pattern switch
    {
        Name.Bunnies => Bunnies,
        Name.Random => Random(),
        _ => throw new NotSupportedException("Pattern not supported")
    };

    private static int[][] Random()
    {
        var randomPattern = Array.Create2D(FixedWidth, FixedHeight);

        randomPattern.FillWithRandomBits();

        return randomPattern;
    }

